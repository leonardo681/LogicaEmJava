public class Pessoa {
    private String nome;

    // Construtor da classe Pessoa
    public Pessoa(String nome) {
        this.nome = nome;
    }

    // Getter para o nome da pessoa
    public String getNome() {
        return nome;
    }

    // Setter para o nome da pessoa
    public void setNome(String nome) {
        this.nome = nome;
    }
}
